function ajax()
{
      var $message=$('#message').val(); //详细描述
      $pass_3=$('#pass_3');
      $.getJSON('__URL__/comment',{message:$message},function(json){
      if(json.status==1)
      { 
        
        $pass_3.css("display","block");
        $pass_3.slideDown(150,function(){     
          }).html('发布成功');
        $('#message').val("");//清空文本域
      }

      else　if(json.status==2)
      { 
        $pass_3.css("display","block");
        $pass_3.slideDown(150,function(){     
          }).html('<nobr>'+'发布失败，内容为空'+'</nobr>');

        $('#message').val("");//清空文本域  
      } 

      else
      { 
        $pass_3.css("display","block");
        $pass_3.slideDown(150,function(){     
          }).html('<nobr>'+'发布失败，是不是没有登录？'+'</nobr>');
        $('#message').val("");//清空文本域  
      } 
    }); //传值
    look();
}


function look()
{
  $pass_3=$('#pass_3');
  $comment=$('#comment');
  $onerror="this.src='__IMAGES__/35.jpg'";
  $.get("__URL__/look",'',function(data){

    if(data[0].message!=count)
    {     
      c++;
      if(c>1) $comment.empty();

      for (var i=0;i<data.length;i++)
      { 
            $reward="reward('回复"+ data[i].nickname +"：')";//回复信息
          //  $comment.css('display','block');   
            $comment.append(
              '<div class="mess">'+

              '<img class="evl_ico" onerror="this.src='+
              $onerror+
              '"  style="float:left" src="__UPLOADS__/thumb_'+
              data[i].img+
              '">'+
              '<small style="line-height:50px; float:left;">'+
                  data[i].nickname+'&nbsp&nbsp&nbsp'+data[i].time+
              '</small>'+

              '<small style="line-height:50px; float:right; margin-right:300px;"><a href="#look" onclick="'+
              $reward+
              '">回复</a></small>'+    
                                     
              '<br><br><br><br>'+
              '<span class="mess_span">'+
                  data[i].message+
              '</span>'+
              '<div class="mess_span_s"></div>'+
              '</div>'
              );

            if(i==0)
            { 
              $(".mess_span,.mess_span_s").fadeOut(600);
              $(".mess_span,.mess_span_s").fadeIn(600);
            }  
      }
      count=data[0].message;
    }

    },'json');
}
